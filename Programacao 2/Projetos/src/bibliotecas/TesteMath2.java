package bibliotecas;

public class TesteMath2 {

	public static void main(String[] args) {
		//Teste random, max, min, ceil, floor
		
		//Gera um double aleatório entre 0 e 1
		System.out.println(Math.random());
		
		//Retorna o maior de dois valores
		System.out.println(Math.max(15, 20));
		
		//Arredonda um double pra cima até virar um int
		System.out.println(Math.ceil(54.65));
		
		//Arredonda um double pra baixo até virar um int
		System.out.println(Math.floor(54.65));
	}
	
}
