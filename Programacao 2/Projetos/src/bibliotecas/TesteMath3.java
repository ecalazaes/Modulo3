package bibliotecas;

public class TesteMath3 {

	public static void main(String[] args) {
		//Teste exp, log, pow, round, sqrt
		
		//Returna o número de Euler elevado a um double
		System.out.println(Math.exp(2));
		
		//Retorna o logaritmo natural de um double
		System.out.println(Math.log(2.32));
		
		//Retorna o valor de um primeiro argumento elevado ao segundo
		System.out.println(Math.pow(2, 3));
		
		//Arredonda um número até o inteiro mais próximo
		System.out.println(Math.round(4.58));
		
		//Retorna o arredondamento correto da raiz quadrada de um double
		System.out.println(Math.sqrt(25));
	}
	
}
